<h1 id="sobre">Sobre o Instituto Federal Catarinense - campus Rio do Sul, Unidade Sede</h1>
<br><br>
<img src="img/sobre.png" alt="" style="height: 400px; margin-left:-900px; border-radius: 20px;">

<p id="txtsobre1">
O atual IFC Campus Rio do Sul foi fundado em 1968 como Colégio Agrícola de Rio do Sul, com foco na 
formação técnica em agropecuária para jovens, principalmente filhos de agricultores. A instituição 
combinava ensino teórico e prático, utilizando sua infraestrutura de fazendas-escola, estufas e laboratórios 
para preparar alunos para o trabalho no campo e apoiar a modernização agrícola na região. Com a transformação 
em Instituto Federal em 2008, o colégio ampliou sua atuação, mas manteve seu legado de excelência nas 
ciências agrárias e compromisso com o desenvolvimento rural sustentável.
<br><br>
O Instituto Federal Catarinense (IFC), Campus Rio do Sul, Unidade Sede, oferece dois cursos técnicos 
integrados ao ensino médio na área agrícola: Agropecuária e Agroecologia.
<br><br>
Curso Técnico em Agropecuária Integrado ao Ensino Médio:
<br><br>
Este curso tem duração de três anos, com carga horária total de 3.600 horas, incluindo 160 horas destinadas 
ao estágio curricular supervisionado obrigatório. 
 O objetivo é formar profissionais com sólida base científica e tecnológica, aptos a atuar em atividades 
 relacionadas à produção vegetal e animal, planejamento e gestão do agronegócio, sempre visando práticas 
 sustentáveis e de menor impacto ambiental. Os egressos estão habilitados a planejar, executar e fiscalizar 
 todas as fases dos empreendimentos agropecuários, além de administrar propriedades rurais e prestar 
 assistência técnica em diversas áreas do setor.
<br><br>

</p>

<img src="img/alojamento-sede.png" alt="" style="height: 400px; margin-right: -850px; border-radius: 20px;">

<p id="txtsobre2">
<br>
Curso Técnico em Agroecologia Integrado ao Ensino Médio:
<br><br>
Com duração de três anos e carga horária total de 3.620 horas, incluindo 160 horas de estágio curricular 
supervisionado obrigatório, este curso visa formar profissionais cidadãos para atuarem em sistemas de produção 
agrícola e pecuária fundamentados em técnicas e princípios agroecológicos. 
 O egresso estará capacitado a promover a sustentabilidade em sistemas de produção animal e vegetal, 
 compreendendo a importância da formação humanística integrada à técnica. As competências desenvolvidas 
 incluem o planejamento e gestão de unidades produtivas sustentáveis, conservação da biodiversidade e 
 atuação em projetos que visem o desenvolvimento socioeconômico e ambiental da região.
<br><br>
Ambos os cursos são oferecidos na modalidade presencial, em período integral, e estão alinhados às demandas 
do mercado por profissionais capacitados em práticas agrícolas sustentáveis e inovadoras. O IFC Campus Rio do 
Sul disponibiliza infraestrutura adequada e corpo docente qualificado para proporcionar uma formação de 
excelência aos estudantes interessados nessas áreas.
</p>
